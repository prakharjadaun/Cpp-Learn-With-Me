/*

Early Binding: 
    - refers to the event that occurs at compile time.
    - It means that an object and a function call are bound during compilation
    - In function overloading, all the overloaded functions are bound to the objects. (because they belong to the same class)
    - These type of function calls are very fast

Late Binding: 
    - refers to the event that occurs at runtime
    - It means that an object and a function call are bound during runtime.
    - In function overriding, overrided function are bound at runtime because it depends which class's (parent or child) object is created.
    - These type of functions calls make your execution somewhat slower.
*/